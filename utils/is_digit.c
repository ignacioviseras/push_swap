# include "./utils.h"

int is_digit(char *s)
{
    int x;
    
    x = 0;
    while (s[x] != '\0')
    {
        if (s[x] == '-' || s[x] == '+' || s[x] == ' ')
            x++;
        if (ft_isdigit(s[x]) == 0)
            return(1);//no es digito
        x++;
    }
    return (0);
}
